package model;
/**
 * PowerupType Model Enum
 * <p>
 * PowerupType describes which type of powerup to put in the game
 * @author Group 1
 *
 */
public enum PowerupType {
	/**
	 * Invincible gives player invincibility
	 */
	INVINCIBLE, 
	/**
	 * Clear removes all hazards from screen
	 */
	CLEAR, 
	/**
	 * Speed gives player superspeed
	 */
	SPEED, 
	/**
	 * Addlife adds a life to player
	 */
	ADDLIFE

	 
}
